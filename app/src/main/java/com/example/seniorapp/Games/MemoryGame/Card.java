package com.example.seniorapp.Games.MemoryGame;

public class Card {
    int cardSign;
    int row;
    int column;


    public int getCardSign() {
        return cardSign;
    }

    public void setCardSign(int cardSign) {
        this.cardSign = cardSign;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getColumn() {
        return column;
    }

    public void setColumn(int column) {
        this.column = column;
    }
}
